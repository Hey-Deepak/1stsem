// Title: to print number pattern
/* Description: to print the numbers of pattern 
                  1
                  2 2
                  3 3 3
                  4 4 4 4   */

//-----------------------------------------------------------------------------
// ------------------------------------------------------------------------------

#include <stdio.h>

int main()
{
    int i, n, j, a = 1;
    printf("enter the length:\n");
    scanf("%d", &n);

    for (i = 1; i <= n; i++)
    {

        for (j = 1; j <= i; j++)
        {

            printf("%d", a);
        }

        a = a + 1;
        printf("\n");
    }
    return 0;
}

